/*
====================================
JavaScript Comments
====================================

Comments are used to explain code.
They are ignored by JavaScript when the program runs.
*/

// -------------------------------
// 1. Single-line Comment
// -------------------------------

// This is a single-line comment
let name = "Harsh"; // This comment is after code
console.log(name);

// -------------------------------
// 2. Multi-line Comment
// -------------------------------

/*
This is a multi-line comment.
It can span multiple lines.
Useful for documentation.
*/

let age = 21;
console.log(age);

// -------------------------------
// 3. Why Comments are Important
// -------------------------------

// Improve code readability
// Help other developers understand logic
// Useful for future you!

// -------------------------------
// 4. Commenting Out Code
// -------------------------------

// console.log("This line will not execute");

let isStudent = true;
// let isStudent = false; // alternate value

console.log(isStudent);

// -------------------------------
// 5. Best Practices
// -------------------------------

// ✔ Write meaningful comments
// ✔ Explain "why", not obvious "what"
// ❌ Avoid unnecessary comments

/*
Bad:
let x = 10; // assigning 10 to x

Good:
let retryCount = 3; // number of retries before failing
*/

console.log("Comments file executed successfully!");
